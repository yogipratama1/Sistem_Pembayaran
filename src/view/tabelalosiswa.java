        String kodesiswa = dataModel.getValueAt(i, 0).toString();
        String namasiswa = dataModel.getValueAt(i, 1).toString();

        CRUDalosiswa lb = new CRUDalosiswa();
        lb.tIDsiswa.setText(kodesiswa);
        lb.tnamasiswa.setText(namasiswa);

        lb.setExtendedState(JFrame.EXIT_ON_CLOSE);
        lb.setVisible(true);
        dispose();
