    private void showJenisPembayaran() {

        DefaultTableModel model;
        Connection con = KoneksiDB.getConnection();
        String[] judul = {"Kode Pembayaran", "Nama Pembayaran", "Biaya", "Kelas", "Periode"};
        model = new DefaultTableModel(judul, 0);
        table_tagihan.setModel(model);
        String sql = "SELECT * FROM tbl_pembayaran where Nama_pembayaran like '%" + txt_cari.getText() + "%'OR Kode_bayar like '%" + txt_cari.getText() + "%' ORDER BY Kode_bayar DESC";

        try {
            Statement stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("Kode_bayar");
                String nama = rs.getString("Nama_pembayaran");
                String bayar = rs.getString("Biaya");
                String kelas = rs.getString("group_kelas");
                String periode = rs.getString("Periode");

                String[] data = {id, nama, bayar.split("\\.")[0], kelas, periode};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        } finally {
            try {
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Seting_tagihan_perkelas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void showKelas() {
        DefaultTableModel model;
        Connection con = KoneksiDB.getConnection();
        String[] judul = {"Kode Kelas", "Nama Kelas", "Grup Kelas"};
        model = new DefaultTableModel(judul, 0);
        table_kelas.setModel(model);
        String sql = "SELECT * FROM tbl_kelas where Nama_kelas like '%" + txt_cari_kelas.getText() + "%'OR group_kelas like '%" + txt_cari_kelas.getText() + "%'"
                + " ORDER BY nama_kelas DESC";

        try {
            ResultSet rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("Kode_kelas");
                String nama = rs.getString("Nama_kelas");
                String gk = rs.getString("group_kelas");

                String[] data = {id, nama, gk};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        } finally {
            try {
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Seting_tagihan_perkelas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void tablearray() {
        int i = table_kelas.getSelectedRow();
        String kodeKelas = table_kelas.getValueAt(i, 0).toString();
        String namaKelas = table_kelas.getValueAt(i, 1).toString();

        DefaultTableModel dataModel = (DefaultTableModel) table_list_kelas.getModel();
        List list = new ArrayList<>();
        table_list_kelas.setAutoCreateColumnsFromModel(true);
        list.add(kodeKelas);
        list.add(namaKelas);

        dataModel.addRow(list.toArray());
    }

    public void simpanTagihan(int rowKelas, String kodeTagihan, String tagihan) {
        Connection con = KoneksiDB.getConnection();
        int i = table_list_kelas.getRowCount();
        int j = 0;
        progress_save.setMaximum(rowKelas);
        while (j < rowKelas) {
            progress_save.setValue(j);
            String kodeKelas = table_list_kelas.getValueAt(j, 0).toString();
            String sql = "UPDATE tbl_dettran SET Hutang='" + tagihan + "' WHERE "
                    + " kode_bayar='" + kodeTagihan + "' AND kode_kelas = '" + kodeKelas + "'";
            System.out.println(sql);
            try {
                con.createStatement().executeUpdate(sql);
            } catch (Exception e) {
                System.out.println(e);
            }
            j++;
        }
        form_progres_save.setVisible(false);
        JOptionPane.showMessageDialog(null, "Berhasil!");
        
        

    }
