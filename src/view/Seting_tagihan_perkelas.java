    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int listKelas = table_list_kelas.getRowCount();
        String tagihanBaru = txt_tagihan_baru.getText().replaceAll("[^0-9]", "");
        String kodeTagihan = txt_id_tagihan.getText();
        if (listKelas > 0 && tagihanBaru.length()>0) {
            Thread t = new Thread(new Runnable() {
                @Override
                public void run() {
                       progress_save.setValue(0);
                    form_progres_save.setLocationRelativeTo(null);
                    form_progres_save.setVisible(true);
                    simpanTagihan(listKelas,kodeTagihan, tagihanBaru);

                }
            });
            t.start();

        } else {
            JOptionPane.showMessageDialog(null, "Pilih Kelas dan isi Tagihan Baru terlebih dahulu!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

