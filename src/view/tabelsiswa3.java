        String kodesiswa = dataModel.getValueAt(i, 0).toString();
        String namasiswa = dataModel.getValueAt(i, 1).toString();
        String kelas = dataModel.getValueAt(i, 2).toString();
        String idkelas = dataModel.getValueAt(i, 3).toString();

        Transaksi lb = new Transaksi();
        lb.tIDsiswa.setText(kodesiswa);
        lb.namasiswa.setText(namasiswa);
        lb.tkelas.setText(kelas);
        lb.tIDkelas.setText(idkelas);
        

        lb.setExtendedState(JFrame.EXIT_ON_CLOSE);
        lb.setVisible(true);
        dispose();
