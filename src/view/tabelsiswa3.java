    public void tampilsiswa() {
        String[] judul = {"Kode Siswa", "Nama Siswa","Kelas","Kode Kelas"};
        model = new DefaultTableModel(judul, 0);
        tblsiswa.setModel(model);
        try {
            String sql = "SELECT *,tbl_siswa.Kode_siswa,tbl_siswa.Nama_siswa,tbl_siswa.Status,tbl_setupkelas.*, tbl_kelas.Nama_kelas"
                    + " FROM tbl_setupkelas INNER join tbl_siswa using(Kode_siswa)"
                    + " inner join tbl_kelas using(kode_kelas)"
                    + " where Nama_siswa like'%" + tcari.getText() + "%' OR Kode_siswa like '%" + tcari.getText() + "' ORDER BY tbl_siswa.Kode_siswa ASC";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                String kodesiswa = rs.getString("Kode_siswa");
                String namasiswa = rs.getString("Nama_siswa");
                String kelas = rs.getString("Nama_kelas");
                String kodekelas=rs.getString("Kode_kelas");

                String[] data = {kodesiswa, namasiswa,kelas,kodekelas};
                model.addRow(data);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

